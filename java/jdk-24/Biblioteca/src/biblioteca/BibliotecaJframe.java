    private final UsuarioDAO usuarioDao = new UsuarioDAO();
    
    private void JButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonActionPerformed
        
        String nome = txtNome.getText().trim();
        String email = txtEmail.getText().trim();
        String telefone = txtTelefone.getText().trim();
        String tipoUsuario = txtTipoUsuario.getText().trim();
        
        if(nome.isEmpty() || email.isEmpty() || telefone.isEmpty() || 
                tipoUsuario.isEmpty()){
            
            JOptionPane.showMessageDialog(this, 
                    "Preencha todos os campos antes de salvar",
                    "Campos obrigatórios",JOptionPane.WARNING_MESSAGE);
            
            return;
        }
        
        Usuario usuario = new Usuario(0, nome, email, telefone, tipoUsuario);
        
        try {
            usuarioDao.criarUsuario(usuario);
            JOptionPane.showMessageDialog(this, "Usuário salvo com sucesso!"
                    ,"Sucesso",
                    JOptionPane.INFORMATION_MESSAGE);
            
        } catch (SQLException ex) {
            
//            Logger.getLogger(getClass().getName()).log(Level.SEVERE, 
//                    "Erro ao salvar usúario",ex);
            
            JOptionPane.showMessageDialog(this, 
                    "Não fois possível salvar o usúario.","Detalhes: "
                            +ex.getMessage(),JOptionPane.INFORMATION_MESSAGE);
            
        
            
        }
        
    }//GEN-LAST:event_JButtonActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            DefaultTableModel model = (DefaultTableModel) tabelMostrar.getModel();
            model.setRowCount(0);
            
            List<Usuario> usuarios = usuarioDao.listarUsuarios();
            
            for (Usuario u : usuarios){
                model.addRow(new Object[]{
                    u.getNome(),
                    u.getEmail(),
                    u.getTelefone(),
                    u.getTipo_usuario()
                });
                
            }
            
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,"Erro ao buscar dados: "+e.getMessage()
                    , "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

